/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART1_UART_Init();
  MX_DMA_Init();
  MX_USART2_UART_Init();
  MX_USART3_UART_Init();
  MX_UART4_Init();
  MX_UART5_Init();
  /* USER CODE BEGIN 2 */
  servos_motormode();
  stop();

  reset_init_pos();

  uint8 operation_char; // Operation received from Zigbee

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
    
    if (HAL_UART_Receive(&huart2, uart_buffer, 1, INTERVAL) == HAL_OK)
    {
      operation_char = uart_buffer[0];
      switch (operation_char)
      {
      case 'w':
        go_forward(SPEED);
        break;
      case 'a':
        turn_left(SPEED);
        break;
      case 's':
        go_forward(-SPEED);
        break;
      case 'd':
        turn_right(SPEED);
        break;
      case 'q':
        turn_left_precise(SPEED, 0);
        break;
      case 'e':
        turn_right_precise(SPEED, 0);
        break;
      case 'z':
        reverse_turn_left(SPEED);
        break;
      case 'c':
        reverse_turn_right(SPEED);
        break;
      case '1':
        m1_left_unit();
        break;
      case '2':
        m1_right_unit();
        break;
      case '3':
        m2_forward_unit();
        break;
      case '4':
        m2_backward_unit();
        break;
      case '5':
        m3_up_unit();
        break;
      case '6':
        m3_down_unit();
        break;
      case '7':
        m4_up_unit();
        break;
      case '8':
        m4_down_unit();
        break;
      case '9':
        grab();
        break;
      case '0':
        drop();
        break;
      case '-':
        reset_init_pos();
        break;
      case '+':
        pause();
        break;
      case ' ':
        stop();
        break;

      default:
        stop();
        pause();
      }
    }
    else
    {
      stop();
      pause();
    }

    // HAL_Delay(INTERVAL);
  }
  /* USER CODE END 3 */
}

